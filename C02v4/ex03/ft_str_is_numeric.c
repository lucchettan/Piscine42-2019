int		ft_str_is_numeric(char *str)
{
	int		value;

	value = 1;
	while (*str != '\0')
		if (!(*str >= '0' && *str <= '9'))
		{
			value = 0;
			return (value);
		}
		else
			str++;
	return (value);
}
